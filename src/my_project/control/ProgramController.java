package my_project.control;

import KAGO_framework.control.ViewController;
import KAGO_framework.model.abitur.datenstrukturen.Queue;
import my_project.model.AutoQueue;
import my_project.model.DriveThru;
import my_project.view.InputReceiver;

import java.awt.event.MouseEvent;

/**
 * Ein Objekt der Klasse ProgramController dient dazu das Programm zu steuern. Die updateProgram - Methode wird
 * mit jeder Frame im laufenden Programm aufgerufen.
 */
public class ProgramController {

    // Referenzen
    private ViewController viewController;
    private Queue<AutoQueue> autoQueue;
    private AutoQueue lastAutoInQueue;

    /**
     * Konstruktor
     * Dieser legt das Objekt der Klasse ProgramController an, das den Programmfluss steuert.
     * Damit der ProgramController auf das Fenster zugreifen kann, benötigt er eine Referenz auf das Objekt
     * der Klasse viewController. Diese wird als Parameter übergeben.
     * @param viewController das viewController-Objekt des Programms
     */

    public ProgramController(ViewController viewController){
        this.viewController = viewController;
    }

    /**
     * Diese Methode wird genau ein mal nach Programmstart aufgerufen. Achtung: funktioniert nicht im Szenario-Modus
     */
    public void startProgram() {
        new InputReceiver(this, viewController);
        new DriveThru(200,200,15,200,viewController);
        autoQueue = new Queue<>();
        lastAutoInQueue = null;
    }

    public void addAutoToQueue(){
        AutoQueue newAutoQueue = new AutoQueue(1100,312, lastAutoInQueue, viewController);
        autoQueue.enqueue(newAutoQueue);
        lastAutoInQueue = newAutoQueue;
    }

    public void deleteAutoFromQueue(){
        if(!autoQueue.isEmpty()){
            if(autoQueue.front().tryToDelete()) autoQueue.dequeue();
        }
    }

    /**
     * Sorgt dafür, dass zunächst gewartet wird, damit der SoundController die
     * Initialisierung abschließen kann. Die Wartezeit ist fest und damit nicht ganz sauber
     * implementiert, aber dafür funktioniert das Programm auch bei falscher Java-Version
     * @param dt Zeit seit letzter Frame
     */
    public void updateProgram(double dt){
    }

    /**
     * Verarbeitet einen Mausklick.
     * @param e das Objekt enthält alle Informationen zum Klick
     */
    public void mouseClicked(MouseEvent e){
    }
}
